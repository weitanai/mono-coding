
export class CusBasicCalculator {

    public constructor(protected value: number = 0) { 
         this.value = value;
    }
    public currentValue(): number {
        return this.value;
    }
    public add(operand: number): this {
        this.value += operand;
        return this;
    }
    public multiply(operand: number): this {
        this.value *= operand;
        return this;
    }
    // ... other operations go here ...
}

